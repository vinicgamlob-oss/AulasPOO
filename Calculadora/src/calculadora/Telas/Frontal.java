    private void btnigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnigualActionPerformed
        n2 =  (int) Double.parseDouble(txtVisor.getText());
        
        
        
       switch (operacoes) {
    case "+":
       resultado =  o.soma(n1, n2);
        break;
    case "-":
       resultado =  o.Sub(n1, n2);
        break;
    case "*":
       resultado =  o.multi(n1, n2);
        break;
    case "/":
        if (n2!= 0) {
            resultado =n1 / n2;
        } else {
            txtVisor.setText("Erro"); // Tratamento simples para não quebrar
            return; 
        }
        break;
} 
        
        txtVisor.setText(String.valueOf(resultado));
        
        
    }//GEN-LAST:event_btnigualActionPerformed

