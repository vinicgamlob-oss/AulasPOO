public class Operacoes {
    
    public double soma(double a ,double b ){
        double Soma = a+b;
        return Soma; 
    }
    
     public double Sub(double a ,double b ){
        double Sub = a-b;
        return Sub; 
    }
     
     public double multi(double a , double b){
         double Multi = a*b;
         return Multi;
     }
     
     public double div(double a , double b){
        double Div = a/b;
         return Div;
     }
    
}
